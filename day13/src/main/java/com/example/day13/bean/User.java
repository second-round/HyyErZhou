package com.example.day13.bean;

public class User {
    private String name;
    private int avatar;

    public void setName(String name) {
        this.name = name;
    }

    public void setAvatar(int avatar) {
        this.avatar = avatar;
    }

    public String getName() {

        return name;
    }

    public int getAvatar() {
        return avatar;
    }
}
